package com.openfog.condo.dsign.watermeter.service;

public class ImportTwiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ImportTwiceException() {

	}

	public ImportTwiceException(String arg0) {
		super(arg0);

	}

	public ImportTwiceException(Throwable arg0) {
		super(arg0);

	}

	public ImportTwiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public ImportTwiceException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
